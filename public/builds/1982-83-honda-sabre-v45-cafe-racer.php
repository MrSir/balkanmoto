                <h2>The Search</h2>
                <p>This was my very first build. My search was on going for pretty much the whole summer of 2018. Kept going back and forth between different motorcycles. Comparing the different characteristics, and trying to narrow things down to what was important to me. Saw a lot of things come up and get sold. But eventually I just decided to pull the trigger. There was a 1983 Honda VF750S (v45 Sabre) available for sale. The guy wanted $800 for it and it said it only had about 30,000km on it. The bike did run and was advertised as needing new fork seals. </p>
                <p>The characteristics of the Sabre that ultimately drew me to it were:
                    <ul>
                        <li>Twin disk brakes in the front</li>
                        <li>Driveshaft</li>
                        <li>Unique motorcycle that you don’t see a lot of (the world didn’t need yet another CB750)</li>
                        <li>Fairly straight frame that will need little modifications</li>
                        <li>Mono-shock rear suspension</li>
                    </ul>
                </p>
                <p>Long story short, there was quite a bit wrong with the bike and I was ready to walk away and in the last moment ended up buying the bike for $400.</p>
                <img src="/builds/img/1982-83-honda-sabre-v45-cafe-racer/the_parts_bike.jpg">

                <h2>The Assessment</h2>
                <p>After brining the bike home and having some time to look it over more in depth and in the light, there was a slew of issues I found with it</p>
                <h3>Mechanical/Modification Issues:</h3>
                <ul>
                    <li>The fork seals were really bad, and a good portion of the fork fluid had leaked out, so they were bottoming out under the slightest of pressure.</li>
                    <li>The stock handlebars were replaced with a straight bar and aftermarket risers, that made it very difficult to put the key in.</li>
                    <li>Carburetors had major air leaks and the bike was running on 2 cylinders only.</li>
                    <li>The clutch and front break levers were snapped off at the ends.</li>
                    <li>The gas cap key hole was completely destroyed and the only way to open it was with a flat head screw driver.</li>
                    <li>The gas tank was repainted in a very poor way.</li>
                    <li>The aftermarket exhaust was rusted away and re-welded.</li>
                </ul>
                <h3>Safety Issues:</h3>
                <ul>
                    <li>The blinkers did not work.</li>
                    <li>The break light and rear blinkers were replaced and not fully working.</li>
                    <li>The mirrors were removed.</li>
                    <li>Headlight was replaced and was pointing about 2 feet in front of the front tired, making it useless.</li>
                    <li>Speedometer was not working.</li>
                    <li>The tires were really old and had pressure cracks all over them.</li>
                    <li>The coolant fluid overflow tank had been broken and was fixed with some plastic glue type mixture.</li>
                </ul>
                <h3>Major Issues:</h3>
                <ul>
                    <li>The bike had been in some sort of accident and the frame was slightly bent near one of the engine mounts. Probably during the same accident the same engine mount was completely snapped off the motor.</li>
                </ul>

                <h2>The Design</h2>
                <p>I started working on a design for the final product. Which would ultimately become my blueprint for what the build will shape up to be.</p>
                <img src="/builds/img/1982-83-honda-sabre-v45-cafe-racer/the_design.jpg">

                <h2>Ill Informed Modifications</h2>
                <p>In my journey to build a cafe racer I was a little too obsessed with the form and not so much the function of the motorcycle, as most new builders are. So I made my first cardinal mistake with the build and ended up cutting the sprints in the forks shorter, in order to shorten the forks.</p>
                <img src="/builds/img/1982-83-honda-sabre-v45-cafe-racer/short_forks.jpg">
                <p>Thankfully it wasn't long before I did some more research and realized what I had done...I had turned an already beaten up machine, into a death cycle. The shortened fork components essentially meant that with the static sag and the already soft springs the bike had only about 1.5" of suspension travel to deal with the road conditions.</p>
                <p>This was the pivotal point at which it become very apparent that this bike is going to be a parts bike and something I practiced on.</p>

                <h2>Restart</h2>
                <p>It took a few more weeks to find a bike that was in much better condition and restart the project. A 1982 Honda V45 Sabre (VF750S) had come up and was in a much much better condition. for a reasonable $1300. The bike had this going for it:
                    <ul>
                        <li>Only 16,874km on the odometer</li>
                        <li>The engine ran, even though it was only running on 3 cylinders.</li>
                        <li>All of the original equipment was on the bike.</li>
                        <li>Had a front fearing and saddle bags on it.</li>
                        <li>All the electronics worked.</li>
                        <li>The exhaust was replaced in 2012 with another stock one in a great condition.</li>
                    </ul>
                </p>
                <p>There were a few little issues, but nothing major. The bike was a solid starting point...and all engine mounts were in place.</p>
                <img src="/builds/img/1982-83-honda-sabre-v45-cafe-racer/the_project_bike.jpg">
                <img src="/builds/img/1982-83-honda-sabre-v45-cafe-racer/the_project_bike_2.jpg">

                <h2>Electronics Upgrades</h2>
                <p>In the coming months I ended up performing the following upgrades to the electronics on the bike:
                    <ul>
                        <li>New LED headlight</li>
                        <li>New Digital Speedometer/Tachometer combo.</li>
                        <li>A speedometer signal adjuster in order to correct the speedo reading.</li>
                        <li>New handlebar controls.</li>
                        <li>Antigravity Battery.</li>
                        <li>New regulator/rectifier to work with the new battery.</li>
                        <li>New taillight with integrated blinkers.</li>
                    </ul>
                </p>

                <h2>Structural/Aesthetic Upgrades</h2>
                <p>The bike in it's stock form was very much a sport touring motorcycle from the 80s. It was always going to need some modifications to make it more of a cafe racer. These are the major changes I had done to it:
                    <ul>
                        <li>Cut the rear of the frame off, and welded in a seat loop.</li>
                        <li>Replaced the stock seat with a cafe racer style humped seat.</li>
                        <li>Built an axle mounted rear fender and licence plate holder.</li>
                        <li>Built new metal side cover plates.</li>
                        <li>Built custom leather cafe racer saddle bags.</li>
                        <li>Replaced the mirrors with bar end mirrors.</li>
                    </ul>
                </p>

                <h2>Maintenance and Performance Upgrades</h2>
                <p>The bike had been neglected in a few areas, and definitely needed some tune ups. These are the items I ended up rebuilding/upgrading over the months.:
                    <ul>
                        <li>Replaced the front break lines with steel braided ones. Replaced the break fluid as well.</li>
                        <li>The clutch slave cylinder was completely rebuilt...a number of times...and the clutch fluid changed out. The clutch fluid line to the lever was also replaced with a steel braided one.</li>
                        <li>The clutch, and front break levers were replaced with adjustable after market ones.</li>
                        <li>The front brakes were cleaned and rebuilt, and break pads were changed.</li>
                        <li>The coolant fluid overflow container was relocated and replaced with a metal bottle.</li>
                        <li>The carburetors were completely rebuilt, gaskets replaced, and cleaned with an ultrasonic cleaner. I also synchronized them.</li>
                        <li>The intake/exhaust valves were adjusted.</li>
                        <li>The clutch plates were replaced, along with the springs.</li>
                        <li>The forks were upgraded with progressive springs and fresh seals, shims, and oil. I also added adjustable preload caps, and setup the static sag on the bike for my weight.</li>
                        <li>The horns were replaced with aftermarket ones.</li>
                        <li>New Continental Conti RoadAttack 3 tires.</li>
                    </ul>
                </p>

                <h2>Conclusion</h2>
                <p>All things said an done to build a modernized cafe racer starting with a bike from the 80s will set you back right about $5,000. Is that worth it? Perhaps not, but than you are faced with the built vs bought dilemma. In my case this was totally worth it, as it gave me an opportunity to learn a ton about how motorcycles actually work, and the proper way to maintain them. I picked up a bunch of new skills like welding, and metal fabrication. As well as brushed up on my electrical systems knowledge. Setting up suspension was a truly eye opening experience. But perhaps most of all the understanding the brilliant engineering behind carburetors and how to properly set them up on a given motorcycle.</p>
