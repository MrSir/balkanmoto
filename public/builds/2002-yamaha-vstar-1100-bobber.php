                <h2>The Search</h2>
                <p>Having learned quite a bit from my previous two builds, I was finally ready to start a proper build. I was looking for a bike that is:
                    <ul>
                        <li>Is a cruiser.</li>
                        <li>Had a good aftermarket support.</li>
                        <li>Had roughly 50hp stock.</li>
                        <li>Had carburetors instead of fuel injection.</li>
                    </ul>
                </p>
                <p>After a few months of research over the winter period I finally concluded my search to a 2001-2008 Yamaha V-Star 1100. The bike was fairly modern, but still ran with carburetors. It looked great even in it's stock form. It was a smaller cruiser which made it perfect for a bobber build. Did not have a ton of power that I'd never use, but had a ton of information on how to add performance. After seeing a few bikes in various states, I found a 2002 model that was being sold by the original owner.</p>
                <img src="/builds/img/2002-yamaha-vstar-1100-bobber/stock.jpg">

                <h2>The Strip Down and Assessment</h2>
                <p>First steps for the bike was to strip it down from the unneeded parts (windshield, saddle bags, and pillion seat), and begin to reconfigure the tail of the motorcycle. I was still very much a novice in fabrication, so I opted for getting a rear fender kit from <a href="https://bluecollarbobbers.com/yamaha-v-star-1100-dragstar/">BlueCollarBobbers</a>. But was set on building my own seat pan, and bobber seat.</p>
                <img src="/builds/img/2002-yamaha-vstar-1100-bobber/bobber_seat.jpg">

                <h2>Performance Modifications</h2>
                <p>Now that the general shape of the bobber was handled I turned my attention to the performance. The Dragstar (as is known) came stock with 49hp and 57ft.lbs of torque. Which was great, and with the reduced weight from the bob job it felt great. But one of the big reasons to go with this bike in first place was the big amount of information available on how to get more out of these motors on the <a href="https://sites.google.com/site/vstar1100kb/home">V-Star 110 Wiki Knowledge Base</a>. Based on the information there I knew that the motor with some straight forward mods can easily be pushed to the 64-67hp range. So I proceeded to add the following mods:
                    <ul>
                        <li>Removed the air box, and converted the carburetors to use POD filters.</li>
                        <li>Fully cleaned, re-jetted, and synchronized the carburetors.</li>
                        <li>Replaced the stock exhaust + cobra slip-ons, to a full 2 into 2 Cobra Speedster Exhaust.</li>
                        <li>Replaced the original tires (yes 2002 tires), with new Dunlop D404 tires</li>
                    </ul>
                </p>

                <h2>Maintenance Modifications</h2>
                <p>As with all bikes the V-Star 1100 isn't perfect. One of the biggest pain points with the design of the motor of this motorcycle is the integrated oil filter. In order to change the oil filter one has to take off the exhaust. This turns a 20min job into a 2 hour endeavour, requiring new gaskets and other consumables. Additionally according to the service manual the valve clearances need to be checked every oil change. The valve adjustment procedure on the motorcycle is quite involved and requires a lot of disassembly.</p>
                <p>In order to solve some of these I ended up doing the following things:
                    <ul>
                        <li>Installed the Baron Custom Accessories Oil Relocation Kit.</li>
                        <li>Adjusted the valves to spec. Which according to the original owner were never done. That's more than 20,000km without adjustment and they were more or less within spec still.</li>
                        <li>Moved the blow by hoses to a separate container and filter instead of back into the carbs in order to keep them clean and allow for a clean out.</li>
                    </ul>
                </p>

                <h2>Aesthetic Modifications</h2>
                <p>After doing a number of the mods, there were still a number of things about the bike that I felt needed to be adjusted, and or redesigned. I ended up performing the following additional modifications:
                    <ul>
                        <li>Removed the Air Induction System. I really did no like the metal hoses wrapping around the motor.</li>
                        <li>Relocated the rear break master cylinder. After the exhaust, and oil relocation kit, it had to be moved elsewhere.</li>
                        <li>Cleaned up the right handlebar by installing an internal throttle, and relocating the ignition switches</li>
                        <li>Relocated the choke lever away from the left handlebar.</li>
                        <li>Fabricated an offset licence plate holder.</li>
                        <li>Installed the Kuryakyn Integrated Torpedo Taillights.</li>
                        <li>New handlebar grips to match the brown on the seat.</li>
                    </ul>
                </p>

