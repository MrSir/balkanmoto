                <h2>Main Path</h2>
                <p>The main trail through the Acton farm land is actually more or less just a dirt road for farm equipment. It is used by the locals as a walking path, and by the off road enthusiasts as a riding ground. The north entrance is just at the east edge of town off of HWY 7. The south entrance is at the south edge of town just off of Churchill Road. The trail is very beginner friendly, with lots of different terrain elements. The one thing that is predominant however is that on drier days the bulk of the road is a soft almost dust like sand that is about 1"-2" deep. This could easily sway you one way or another. Just off of the main path towards loop 1, there is a slightly bigger hill with a steeper incline. It's a great place to practice going up and down. The south side of the hill is a bit more challenging with a very long straight descend at about 20deg incline. What makes it more challenging is the large number of loose medium size rocks, that make choosing your line a bit more interesting.</p>

                <h2>Loop 1: Beginner Loop</h2>
                <p>The first loop starts on the flat training ground area and follows the single track towards the forest. The loop itself is fairly short, but gets you exposed to some single track, some fairly easy hills, and some sand and medium size rock filled areas. All in all quite easy, but a solid spot to practice fundamentals and work on slow speed maneuvers.</p>

                <h2>Loop 2: Beginner/Intermediate Loop</h2>
                <p>The second loop shares a part of the main path with loop 1. It veers off through a sandy forest path towards one of the farm fields to the east. Going through the field is a straight path following the perimeter, that is fairly grassy, so traction could be a fun surprise to the throttle happy. The north end of the loop goes through another tight forest section that leads back to the main path.</p>

                <h2>Loop 3: Beginner Dual Track Loop</h2>
                <p>The third loop shares the first part of the main path, then veers off to the left. It does a full loop following the border of the farm land back to the entry point of the main path off of HWY 7. A good portion of the loop is on a dual track surface, with fairly deep tracks. So pick left or right and stick with it. There are a few overhanging trees that you may need to duck for if standing.</p>

                <h2>Advanced Section</h2>
                <p>On the south end of the main path you will find another path leading down towards the forest. This will lead oyu into the more advanced section of the trails. There you will find much more challenging terrain, varying from steep narrow sandy descends, to full blown sand pits, to enduro level hill climbs.</p>

