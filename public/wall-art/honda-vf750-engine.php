                <h2>The Idea</h2>
                <p>After completing my <a href="/builds/1982-83-honda-sabre-v45-cafe-racer.php">1982-83 Honda Sabre V45 Cafe Racer - The Cafe Sabre</a> build, and deciding to sell off the bike I still had the parts bike sitting around. It was definitely not in a repairable condition, and not one was going to buy it in that state. So I figured I'd use the spare motor as a learning tool, and completely disassemble it to see how it worked. Then I'd keep the internals out and re-assemble the cleaned shell to make a wall piece out of it.</p>

                <h2>The Processes</h2>
                <p>The first challenge was getting the motor out of the frame. Supporting the motor from the bottom and unbolting the frame then sliding it up and over the motor was the easy part. Getting the motor off the ground and onto my work bench was a different story. The engine easily weights in at 120-150lbs...which isn't a terrible amount of weight to lift...but it is a very odd v4 shape that has no good place to properly grip onto. Slowly but surely I built progressively higher steps and lifted it up to where it only needed a couple of more inches to be hoisted onto the bench.</p>
                <p>Breaking down the motor took a few specialty tools, and was very insightful. The honda engineers truly have built something incredible here. It took another couple of days for me to fully break it down and start putting it back together. Since I was planning on putting it on a shelf I really did not want this thing and it's full weight hanging over peoples heads. So I left the majority of the internal parts out of it. I only put parts back in that were needed to re-assemble the the exterior panels.</p>
                <p>I used a dremel kit with various brushes to polish and clean the exterior of the block. This really made the whole motor pop. There was a lot of gunk on it from the last 40 years.</p>

                <h2>The Shelf</h2>
                <p>The shelf consisted of a piece of a wooden LV Beam (2"x6"). Here in north america they use these to hold up roofs of houses, so I figured it be strong enough to have a heavy engine sit on it. As for the supports I ended up grabbing a pair of heavy duty shelf arms that when properly installed can carry up to 800lbs. I really did not want this thing falling down. After installing the shelf on the wall I tested it by adding weights onto it, slowly increasing the load up to 80lbs. It was holding fine, so it was time to hoist the now clean motor onto it. I made a small bracket to attach the motor to the shelf, in case some one accidentally pushed it.</p>

