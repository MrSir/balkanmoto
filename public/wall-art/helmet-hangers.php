                <h2>The Idea</h2>
                <p>It was time for me to upgrade my helmet for the 3rd time, and I now had 2 old helmets with no room to store. So like any other experienced rider i decided to keep them instead of trash them and turn them into wall art.</p>
                <p>The plan was simple buy some hardware plumbing parts and some wood and make 90 deg stands that can be mounted on the wall and helmets be placed on them.</p>

                <h2>The Design</h2>
                <p>The materials needed for the 1 helmet hanger are:
                    <ul>
                        <li>2x Black Iron 1/2" Floor Flange Fitting (round or square)</li>
                        <li>2x Black Steel 1/2" x 6" Pipe</li>
                        <li>1x Black Iron 1/2" 90 Degree Elbow (female on both sides)</li>
                        <li>1x 1.5"x3.5"x3.5" piece of wood (cut from 2"x4")</li>
                        <li>1x 6.5"x3.5" piece of leather to wrap the wood</li>
                        <li>1x Can of Black Gloss spray paint</li>
                    </ul>
                </p>
                <p>The pipe pieces can be pre-assembled before mounting on the wall. They don't need to be tightened a terrible amount, hand tight is fine. The spray paint is mostly for the wood, but one could spray the metal as well to get a more uniform look. In my case I lef the pipes their original color. Finally the leather wrap for the wood is more to protect the inside of the helmet lining from the flatness of the wood and it's sharp sides. If you have the tools you could machine the wood piece to a more round shape and skip the leather all together. I found this to be a good inexpensive compromise, and I already had the tools and dies for the leather.</p>

