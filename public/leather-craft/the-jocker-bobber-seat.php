                <h2>The Idea</h2>
                <p>For my <a href="/builds/2002-yamaha-vstar-1100-bobber.php">2002 Yamaha V-Star 1100 Bobber - The Jocker Bobber</a> build I knew I wanted a retro style spring supported bobber seat, from my original designs. Having done some fabrication and leather work already on my previous builds I figured this would be a nice little project to take my skills further. I needed to make a seat pan, and then build a seat around it.</p>
                <p>I drew inspiration for the design from the seats available on the <a href="https://bluecollarbobbers.com">Blue Collar Bobbers</a> catalog, as well as the custom seat made by Spirit Leather for one of the Bobber Brothers Projects.</p>
