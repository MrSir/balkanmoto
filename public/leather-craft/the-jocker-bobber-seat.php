                <h2>The Design</h2>
                <p>My design used a similar pattern with 2 kidney shaped patches on the top side of the seat in black, and brown leather all around them for the main body. Of course I started with bare vegetable tan leather, and died it myself. The bottom of the seat pan was wrapped in leather as well and died black. As for the interior of the seat I opted for putting in a Saddlemen Large Gel Pad with a bunch of carpet underlining foam I had laying around.</p>

