                <h2>The Idea</h2>
                <p>After completing the first phase of my <a href="/builds/1982-83-honda-sabre-v45-cafe-racer.php">1982-83 Honda Sabre V45 Cafe Racer - The Cafe Sabre</a> build, I wanted to make the bike look even more old school with some custom leather saddlebags. I had looked online for commercially available bags, and nothing really fit what I was looking for. The triangular gap under the seat that the custom metal cover plates were covering was pretty large, and pretty much nothing available was that big. If it was that big it was really wide so it would push the rider's feet out wider than they need to be. So make my own is what it came down to.</p>

                <h2>The Template</h2>
                <p>After some research and some photoshop sketches I came up with a simple template. Of course you would need 2 of them with the main body mirrored horizontally to work on the opposite side of the bike. While the holes for the straps are placed to work with the stock frame of the Honda V45, they may also work for your bike...or just put them where you see fit in order to work.</p>
